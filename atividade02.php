<?php
  
    $n1 = 1500;
    $n2 = 3000;
    $operacao = "*";

    switch ($operacao) {
        case '/':
            echo ($n1 / $n2);
            
            break;
        case '*':
            echo ($n1 * $n2);
            
            break;
        case '-':
            echo ($n1 - $n2);
            
            break;
        case '+':
            echo ($n1 + $n2);
            
            break;
    }

    

?>